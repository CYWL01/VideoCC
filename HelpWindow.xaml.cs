## 匹配系统详解

### 第一轮：文件名评分匹配

程序通过 14 种方式提取文件名中的集号，包括 `S01E02`、`第1集`、`第一集`、`[01]`、`-01-` 等（13 种正则 + 中文数字解析）。

评分公式（满分约 255 分）：

| 项目 | 满分 | 说明 |
|------|------|------|
| 基础分 | 100 | 集号匹配即得 |
| 季数加分 | 35 | 双方都有季号 +35，一方有 +10，都无+5 |
| 剧名匹配加分 | 120 | 双方文件名剧名一致得最高分 |

剧名匹配分档：

| 条件 | 分数 |
|------|------|
| 文件名剧名精确匹配 | 120 |
| 目录剧名重叠 | 90 |
| 文件剧名匹配对方目录剧名 | 85 |
| 双方无剧名（纯集数匹配） | 15 |
| 一方有剧名，无冲突 | 8 |
| 同集多部不同剧 | 跳过（不匹配） |

