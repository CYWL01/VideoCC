**匹配流程：**

1. 扫描完成后，收集所有未被文件名评分匹配的视频
2. 有集号的按集分组，无集号的（电影/单文件）作为一组
3. 每组一次 API 调用，将组内所有视频和字幕名称发给 AI
4. AI 先翻译非中文名称为中文，再比对判断是否同一作品
5. 返回配对结果：置信度 ≥ 0.5 标注「AI 智能匹配」，0.3~0.5 标注「⚠ 疑似匹配」并高亮

**跨语言支持：** AI 支持跨语言匹配：中 ↔ 英 ↔ 日 ↔ 韩 ↔ 法 ↔ 德 ↔ 俄 ↔ 西 ↔ 葡等。

